const mB = (days: number, mag = 5): DramaTemplate["effect"] => ({ type: "morale-boost", duration: days, magnitude: mag });
const mP = (days: number, mag = 5): DramaTemplate["effect"] => ({ type: "morale-penalty", duration: days, magnitude: mag });
const rB = (days: number, mag = 4): DramaTemplate["effect"] => ({ type: "rating-boost", duration: days, magnitude: mag });
const rP = (days: number, mag = 4): DramaTemplate["effect"] => ({ type: "rating-penalty", duration: days, magnitude: mag });

