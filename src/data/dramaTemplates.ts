// Drama Engine — Pre-written event template library.
// Templates use these placeholders, filled in by the engine:
//   {P}   primary player name (e.g. "Rodriguez")
//   {F}   player first name
//   {T}   primary team abbreviation (e.g. "Tigers")
//   {Tc}  team city ("Detroit")
//   {Mgr} manager name
//   {N}   small random number 1-20
//   {Days} small random number 1-7
//
// Each template carries category, severity, tone, emoji, and an optional
// gameEffect that the engine applies to the involved player/team.
//
// This is the fallback library — it must work great without any API key.

export type DramaCategory =
  | "injury" | "funny" | "rumor" | "milestone" | "drama"
  | "personal" | "weather" | "lucky" | "cold-streak" | "hot-streak"
  | "comeback" | "pop-culture" | "holiday" | "rivalry" | "hot-take";

export type DramaTone = "playful" | "dramatic" | "serious" | "absurd";
export type DramaSeverity = "minor" | "moderate" | "major";

export interface DramaTemplate {
  id: string;
  category: DramaCategory;
  severity: DramaSeverity;
  tone: DramaTone;
  emoji: string;
  /** Headline template using {P}, {T}, {F}, {Tc}, {Mgr}, {N}, {Days}. Under 80 chars after fill. */
  headline: string;
  /** Short ticker variant (under 50 chars). */
  ticker: string;
  /** Optional 1-2 sentence story. */
  body?: string;
  /** Game effect applied to the involved player or team. */
  effect?: {
    type: "injury" | "rating-boost" | "rating-penalty" | "morale-boost" | "morale-penalty" | "none";
    duration: number;
    magnitude: number;
    attribute?: string;
  };
  /** True if this template needs a player slotted; false if team-only or league-wide. */
  needsPlayer?: boolean;
  /** True if eligible to be promoted to "Memorable Moment". */
  memorableEligible?: boolean;
}

